    private void btnregistrarMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarMateriaActionPerformed
    // TODO add your handling code here:
    String idMateria = txtidmateria.getText();
    String nomMateria = txtmateria.getText();

    if (idMateria.isEmpty() || nomMateria.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Debe completar todos los campos");
    } else {
        m.setIdMateria(idMateria);
        m.setNomMateria(nomMateria);

        if (daoM.insertar(m)) {
            JOptionPane.showMessageDialog(null, "Materia Registrada Con Exito");
            limpiarDatosMateria();
            limpiarTablaMateria();
            listarMateria();
        } else {
            JOptionPane.showMessageDialog(null, "Error al Registrar la Materia");
        }
    }
    }//GEN-LAST:event_btnregistrarMateriaActionPerformed


    private void btnregistrarempleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarempleadoActionPerformed
        // TODO add your handling code here:
            try {
            // Obtener el texto del campo de texto y convertirlo a entero
            int ci = Integer.parseInt(txtci.getText());
            int telefono = Integer.parseInt(txttelefono.getText());
            // Asignar el valor al objeto cr
            em.setCi(ci);
            em.setTelefono(telefono);
            em.setNombre(txtnomempleado.getText());
            em.setApellido(txtapempleado.getText());
            em.setCorreo(txtcorreo.getText());
            em.setIdCargos(Integer.parseInt(txtidcargoempleado.getText()));
            em.setIdMateria(txtidmateriaempleado.getText());

        } catch (NumberFormatException e) {
            // Manejo de error si el texto no puede ser convertido a un entero
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un número válido.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        if(daoEm.insertar(em)){
            JOptionPane.showMessageDialog(null,"Empleado Registrado con exito");
            limpiarDatosEmpleado();
            limpiarTablaEmpleado();
            listarEmpleado();
        }else{
            JOptionPane.showMessageDialog(null,"Error al Registrar ");
        }
    }//GEN-LAST:event_btnregistrarempleadoActionPerformed

